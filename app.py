def main():
    """Run the alert triage pipeline."""
    # Configuration (configurable for tuning)
    alert_file = "mock_alerts.json"
    risk_threshold = 60.0  # Tune this value to adjust sensitivity
    
    # Optional: Custom weights for different environments
    custom_severity_weights = {
        'low': 20,
        'medium': 50,
        'high': 90
    }
    
    custom_asset_weights = {
        'standard': 10,
        'important': 50,
        'critical': 100
    }
    
    try:
        # Load alerts
        print(f"Loading alerts from {alert_file}...")
        alerts = load_alerts(alert_file)
        print(f"Loaded {len(alerts)} alerts\n")
        
        # Initialize scoring engine with configuration
        engine = ScoringEngine(
            severity_weights=custom_severity_weights,
            asset_weights=custom_asset_weights,
            frequency_factor=0.1,
            risk_threshold=risk_threshold
        )
        
        # Score and prioritize alerts
        scored_alerts = engine.score_alerts(alerts)
        
        # Print report
        print_triage_report(scored_alerts, risk_threshold)
        
        # Generate HTML report
        generate_html_report(scored_alerts, risk_threshold, "alert_triage_report.html")
        
        # Optional: Return top critical alerts
        critical_alerts = [sa for sa in scored_alerts if sa.priority == "Critical"]
        if critical_alerts:
            print(f"\n[ACTION] {len(critical_alerts)} critical alert(s) require immediate investigation.")
        
    except FileNotFoundError as e:
        print(f"Error: {e}")
    except Exception as e:
        print(f"Error during triage: {e}")


if __name__ == "__main__":
    main()
